function soma (a: number, b: number): number {
    return a + b
}

function sub (a: number, b: number): number {
    return a - b
}

function multi (a: number, b: number): number {
    return a * b
}

function div (a: number, b: number): number {
    return a / b
}

export { soma, sub, multi, div }
